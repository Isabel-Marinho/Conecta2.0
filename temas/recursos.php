    <div class="conteudo_proj div-recursos" id="sider2">
            <h1 class="titulo_projeto"> Tipos e Formas de Repasse de Recursos aos Municípios </h1>
            <br><br>
            <h1 class="titulo_projeto"> Transferências Constitucionais:  </h1>
            <ul class="conteudo-lista">
            
            Essas são parcelas de recursos arrecadados pelo Governo Federal e repassados aos municípios de acordo com o que está estabelecido na Constituição Federal. Entre os principais repasses estão o Fundo de Participação dos Estados e do Distrito Federal (FPE), o Fundo de Participação dos Municípios (FPM) e outros.
            
            </ul>
            <br>

            <h1 class="titulo_projeto"> Transferências Voluntárias:   </h1>
            <ul class="conteudo-lista">
            Definidas pela Lei de Responsabilidade Fiscal, são recursos entregues a outro ente da Federação para cooperação, auxílio ou assistência financeira. Podem ser formalizadas por meio de convênios ou contratos de repasse.
            </ul>
            <br>

            <h1 class="titulo_projeto"> Transferências Legais:   </h1>
            <ul class="conteudo-lista">
            
            Consistem em repasses de recursos do Governo Federal para Estados, Distrito Federal e Municípios, regulamentadas por leis específicas. Podem ser vinculadas ou não a um fim específico.
            
            </ul>
            <br>

            <h1 class="titulo_projeto"> Dicas para Captação de Recursos: </h1>
            <ul class="conteudo-lista">
            <li>
            Explore as oportunidades oferecidas pelas transferências voluntárias, buscando estabelecer convênios ou contratos de repasse para a execução de programas e projetos de interesse mútuo.
        </li><li>
        Esteja atento às emendas parlamentares e às propostas do Poder Executivo no Orçamento Geral da União, buscando recursos para iniciativas relevantes para o seu município.
        </li><li>
        Avalie a possibilidade de participar de programas que oferecem transferências automáticas, especialmente na área da educação, como o Programa Nacional de Alimentação Escolar (PNAE) e o Programa Dinheiro Direto na Escola (PDDE).
        </li><li>
        Considere o uso da transferência fundo a fundo como uma forma eficaz de descentralização de recursos, aproveitando fundos específicos como o Fundo Nacional de Saúde (FNS) e o Fundo Nacional da Assistência Social (FNAS).
        </li><li>
        Nossa plataforma visa simplificar o processo de captação de recursos, fornecendo informações claras e direcionadas para que os gestores municipais possam aproveitar ao máximo as oportunidades disponíveis. Impulsione o desenvolvimento de seu município!
        </li>
            </ul>
