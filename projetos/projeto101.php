            <h1 class="titulo_projeto"> Núcleo Integrado de Pacientes Neurocirúrgicos com Distúrbios do Movimento (NIPNDIM) – Tratamento Cirúrgico da Doença de Parkinson. </h1>
            <br>
                <p class="conteudo">
                A Doença de Parkinson é uma doença progressiva e incurável. Dados da Organização Mundial de Saúde (OMS) mostram que aproximadamente 1% da população mundial com idade superior a 65 anos tem a doença. 
                Pode ocorrer mais precocemente a partir de 30-40 anos de idade, normalmente ligada a fator hereditário. É caracterizada basicamente por tremor de repouso, instabilidade postural, rigidez e lentidão nos movimentos. 
                Há também outros sintomas não motores, como a diminuição do olfato, distúrbios do sono, alteração do ritmo intestinal e depressão. O tratamento consiste na realização de fisioterapia, terapia ocupacional e tratamento medicamentoso. 
                Sabe-se que após alguns anos, à medida que a doença progride, esse tratamento começa a falhar. A necessidade de aumento progressivo das doses da medicação e a redução do seu intervalo de administração começam a provocar efeitos colaterais graves. 
                O paciente vai apresentar flutuações clínicas relacionados à ação inconstante dos medicamentos. Começara a intercalar períodos de completa imobilidade, com períodos de movimentos espontâneos e involuntários.<br><br> 
                Essa flutuação dos sintomas causam a completa incapacidade funcional para o trabalho e limitam o convívio social. Nesse estágio a única solução é atuar diretamente nos núcleos cerebrais que modulam os movimentos do corpo. 
                Existem duas técnicas de atuar diretamente nesses núcleos cerebrais, a lesão ou a estimulação. Na estimulação, um aparelho tipo marcapasso, com um gerador, implantado no subcutâneo do paciente, irá emitir impulsos elétricos 
                para o cérebro através de eletrodos implantados nos núcleos cerebrais. A estimulação tem a vantagem, em relação a lesão, de poder ser regulado para menos ou mais de acordo com a necessidade do paciente. 
                A estimulação dos núcleos centrais do cérebro permite uma importante redução das flutuações clínicas, dos efeitos colaterais e, dependendo do foco de estimulação, a redução das medicações. 
                O projeto tem como objetivos principais:<br><br> 1) promover esclarecimentos ao paciente e à família sobre a doença de Parkinson e sua evolução. <br><br>2) auxiliar na formação de neurologistas especializados no tratamento da doença de Parkinson; <br><br>
                3) orientar sobre a necessidade de reajuste de doses medicamentosas; <br><br>4) realizar o implante de estimulação cerebral profunda, quando indicado; <br><br>5) acompanhar ambulatorialmente para ajustes na estimulação cerebral após o implante e; <br><br>
                6) dar suporte ao desenvolvimento de projetos de pesquisa nessa área.

