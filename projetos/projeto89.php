            <h1 class="titulo_projeto"> Efeitos da identidade gênero e orientação sexual sobre a notificação e mortalidade por violências </h1>
            <br>
                <p class="conteudo">
                Mundialmente, as travestis e mulheres transexuais convivem com situações extremas 
                de vulnerabilidade, particularmente no cenário brasileiro, em que o grupo é altamente 
                estigmatizado. O Brasil ocupa o primeiro lugar em todo o mundo em homicídios contra pessoas 
                trans. Apesar da extrema vulnerabilidade, são escassos os estudos sobre os registros de 
                violência e óbitos. As poucas estatísticas produzidas se sustentam a partir de voluntários
                que coletam informações em notícias de jornais e em prontuários de hospitais sensíveis ao tema.<br><br>
                Ao examinar a sobreposição entre a orientação sexual e a identidade de gênero essa a lacuna 
                do conhecimento é ainda mais dramática. Não existem estimativas baseadas em sistemas de 
                informações populacionais. A presente proposta pretende investigar a hipótese que mulheres 
                trans não-heterossexuais tem maior probabilidade de morrer (por todas as causas e por causas 
                violentas) que os demais grupos de mulheres e, mesmo sendo vítimas de violência, 
                tem menor probabilidade de detecção pelos serviços de saúde. Pelos motivos expostos, 
                a hipótese que a conjugação desses fatores impute riscos ainda pouco reconhecidos 
                pela comunidade acadêmica parece ser plausível.<br><br> Utilizando dados das coortes de mulheres 
                (travestis e trans) do Instituto Nacional de Infectologia Evandro Chagas e os sistemas de 
                notificação de violências e de mortalidade, será realizado um relacionamento de bancos de 
                dados que permita examinar a hipótese central do estudo. Ao realizar o processo de 
                relacionamento por técnica de linkage probabilístico será possível reconhecer os efeitos 
                da identidade de gênero e orientação sexual sobre a mortalidade e fatores relacionados 
                aos registros, bem como desenvolver metodologias incorporáveis aos serviços de vigilância em 
                saúde do país. Com isso, a presente proposta poderá apoiar a formulação de políticas públicas 
                para o enfrentamento da violência de gênero.
                </p>
               
