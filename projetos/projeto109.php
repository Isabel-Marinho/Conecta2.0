                Criar e gerenciar bancos de dados, com a utilização de modelos matemáticos e interpretação estatística como forma de prevenir lesões e observar a evolução do desempenho atlético auxiliando a tomada de decisão. <br><br>
                O banco de dados servirá ainda como modelo para o desenvolvimento de novas gerações de atletas; <br><br>
                Estruturar um programa de capacitação e formação de recursos humanos direcionado aos profissionais envolvidos com as áreas da ciência do esporte; <br><br>
                Promover cooperação técnica e científica com outros Centros de Alto Rendimento nacionais e internacionais; <br><br>
                Criar indicadores de monitoramento de direitos das mulheres no futebol, visando fortalecimento das redes locais e na maior participação nos espaços de controle e deliberação de políticas públicas; <br><br>
                Incidir politicamente na conscientização acerca do acompanhamento público das discussões de todas as etapas normativas vinculadas ao futebol feminino e garantias de direitos das atletas e equipes técnicas.<br><br>
                Resultados esperados: Reduzir a incidência de lesões, assim como o tempo de afastamento de atletas dos treinos e competições; <br><br>
                Contribuir para a melhoria do desempenho das atletas quando comparados com as últimas competições; <br><br>
                Atender as necessidades de diagnóstico do programa de descoberta, formação e aperfeiçoamento de talentos do esporte; <br><br>
                Ser reconhecido como um polo de referência nacional no desenvolvimento do esporte e de pesquisas voltadas à ciência aplicada ao esporte; <br><br>
                Estabelecer e fortalecer parcerias com a rede de apoio a mulheres, identificando programas e movimentos sociais voltados à política de atendimento e garantia de direito,
