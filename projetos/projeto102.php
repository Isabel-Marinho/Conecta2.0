                <p class="conteudo">
                A doença da coluna vertebral é um grande problema de saúde pública. Globalmente, o número de anos vividos com incapacidade causada pela dor lombar aumentou 54% entre 1990 e 2015, 
                principalmente por causa do aumento populacional e do envelhecimento, com o maior aumento observado nos países de baixa e média renda. A dor lombar é atualmente principal causa de incapacidade em todo o mundo. 
                Na grande maioria das pessoas com dor lombar, não é possível identificar a causa da dor. Apenas uma pequena proporção de pessoas tem a causa bem definida como a fratura, malignidade ou infecção da coluna vertebral.
                Uma revisão sistemática realizada por Meucci e cols (2015), revelou que a prevalência de dor lombar crônica foi de 4,2% em indivíduos entre 24 e 39 anos, e de 19,6% naqueles com idade entre 20 e 59 anos. 
                Dos nove estudos com indivíduos com 18 anos ou mais, seis relataram dor lombar crônica entre 3,9% e 10,2% e três, prevalência entre 13,1% e 20,3%. Na população idosa brasileira, a prevalência crônica de dor lombar 
                foi de 25,4%.<br><br> Segundo dados globais esta prevalência permanece na tendência de aumento por diversas questões como transtornos psicológicos e múltiplos problemas médicos, incluindo obesidade, tabagismo, falta de exercício, 
                aumento da idade e fatores de estilo de vida. (Hoy e cols., 2012, Manchikanti e cols., 2014). Deve se levar em consideração também o sucesso limitado na identificação de tratamentos eficazes pela grande variação nas manifestações, 
                possível manutenção de fatores precipitantes, prognóstico e consequências em termos de interferência de atividade e qualidade de vida (Vlaeyen e cols.). Esses dados nos alertam para uma enorme prevalência de sintomas que podem ser 
                um campo fértil para indicação de cirurgias da coluna vertebral. Isso poderia explicar a presença de 7800 pacientes na fila da SES com indicação da de cirurgia de coluna do Estado do Rio de Janeiro em agosto de 2018, fator motivador 
                do Projeto NIPNAC e que reduziu a fila para 2500 paciente após 4 anos de funcionamento do projeto. O projeto tem como objetivos principais: <br><br>1) realizar a triagem dos pacientes com indicação para a cirurgia da coluna vertebral, 
                principalmente relacionadas às doenças degenerativas da coluna vertebral; <br><br>2) realizar as cirurgias da coluna vertebral dos pacientes selecionados; <br><br>3) acompanhar ambulatorialmente os casos operados; <br><br>4) dar suporte ao desenvolvimento 
                de projetos de pesquisa nessa área.

