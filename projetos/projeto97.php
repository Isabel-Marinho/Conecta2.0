            <h1 class="titulo_projeto"> O Turismo Pedagógico como instrumento de apoio à educação básica </h1>
            <br>
                <p class="conteudo">
                Este projeto parte da ideia de que o turismo é um fenômeno social que transcende a ação de viajar. Ele é capaz de proporcionar o encontro entre pessoas de diferentes idades e culturas, 
                um elemento transformador do espaço, uma das formas de geração de renda que mais crescem no mundo e, entre outras coisas, nos faz enxergar o que está além daquilo que a paisagem nos mostra imediatamente. 
                No entanto, em países com grande desigualdade social, a maioria das pessoas não costuma ter acesso aos principais espaços turistificados do seu lugar de moradia, ficando, assim, próximas fisicamente do patrimônio 
                natural e cultural coletivo, porém distantes socialmente deles.<br><br> 
                
                Neste sentido, nos perguntamos: que tipo de Turismo queremos? Como ele pode ser alcançado? De que maneira ele é capaz de contribuir com o desenvolvimento 
                territorial e com a promoção da cidadania? 
                <br><br>
                O turismo precisa ser pensado, planejado e executado a partir dos lugares que são transformados ou se pretende transformar em destinos turísticos. Para isso a participação 
                consciente da população local é imprescindível. Neste sentido, para se pensar em atrativos autênticos e que, de fato, representem o território (terreno, pessoas, história) acreditamos ser necessário, antes de tudo, 
                que os moradores conheçam o município onde residem e a região da qual este faz parte, a fim de se sentirem identificados com ele, cuidarem dos seus espaços públicos, patrimônios naturais e culturais e terem vontade 
                de mostrá-lo aos seus visitantes.
                <br><br> Um dos caminhos possíveis para se promover este conhecimento e reconhecimento do município pela sua população é o do Turismo Pedagógico, entendido aqui como um método de ensino 
                capaz de despertar a curiosidade de cidadãs e cidadãos em fase escolar e fortalecer o sentimento de pertencimento por meio de atividades pedagógicas que apoiem o processo ensino-aprendizagem. Mas, para isso, 
                é necessário que os profissionais da educação conheçam o método e possam aplicá-lo em suas unidades escolares de maneira adequada. Para isso, oferecemos, gratuitamente, o curso de extensão em Turismo Pedagógico 
                a profissionais da educação básica pública, a fim de dar-lhes a formação necessária para aplicar com seus estudantes. Além disso, apoiamos a realização das atividades de visita a lugares turistificados do município 
                onde a escola se localiza. Temos realizado esse trabalho no estado do Rio de Janeiro há 10 anos, oferecendo cursos de extensão e acompanhando os profissionais da educação em suas atividades pedagógicas fora da escola 
                e temos obtido grandes resultados, que foram reconhecidos com dois prêmios: um nacional - o Prêmio Braztoa de Sustentabilidade 2016/201/ - e um estadual - o Prêmio Paulo Freire concedido pela Alerj.
                </p>
               
